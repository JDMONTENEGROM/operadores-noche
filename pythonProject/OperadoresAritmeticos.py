numero1=int( input("ingrese el numero 1: "))
numero2=int(input("ingrese el numero 2: "))
suma = numero1+numero2

print(f"la suma de {numero1} y {numero2} es: {suma} " )
print(f"la resta de {numero1} y {numero2} es: {numero1-numero2} " )
print(f"la divsion de {numero1} y {numero2} es: {numero1/numero2} " )
print(f"la multiplicacion de {numero1} y {numero2} es: {numero1*numero2} " )
print(f"la modulo de {numero1} y {numero2} es: {numero1%numero2} " )
print(f"la divicion Estera  de {numero1} y {numero2} es: {numero1//numero2} " )





